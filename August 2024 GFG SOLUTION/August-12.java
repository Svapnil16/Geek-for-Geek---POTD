class Solution {
    public int SumofMiddleElements(int[] arr1, int[] arr2) {
        // code here
        PriorityQueue<Integer>pq = new PriorityQueue<>();
        for(int a : arr1)
        pq.add(a);
        for(int a : arr2)
        pq.add(a);
        int combination[] = new int[2*arr1.length];
        int index = 0;
        while(!pq.isEmpty())
        combination[index++] = pq.poll();
        
        return combination[combination.length/2] + combination[(combination.length/2)-1];
    }
}

